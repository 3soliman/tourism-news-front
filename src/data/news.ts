export function getNewsBySlug(slug: string) {
  return news.find((article) => article.slug === slug);
}

export function getLatestNews(limit = 3) {
  return news.slice(0, limit);
}

export function getNewsByCategory(categorySlug: string) {
  return news.filter((article) => article.categorySlug === categorySlug);
}

export function getNewsByAuthor(authorSlug: string) {
  return news.filter((article) => article.authorSlug === authorSlug);
}

export function getRecentNews(hours = 48) {
  const cutoff = Date.now() - hours * 60 * 60 * 1000;
  return news.filter(
    (article) => new Date(article.publishedAtISO).getTime() >= cutoff,
  );
}

export function getPopularNews(limit = 5) {
  return [...news].slice(0, limit);
}
